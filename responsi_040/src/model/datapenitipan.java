public class datapenitipan {
    private int id;
    private String namap;
    private String namah;
    private String jenish;
    private String telp;
    private String durasi;
    private String total;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNamap() {
        return namap;
    }

    public void setNamap(String namap) {
        this.namap = namap;
    }

    public String getNamah() {
        return namah;
    }

    public void setNamah(String namah) {
        this.namah = namah;
    }

    public String getJenish() {
        return jenish;
    }

    public void setJenish(String jenish) {
        this.jenish = jenish;
    }

    public String getTelp() {
        return telp;
    }

    public void setTelp(String telp) {
        this.telp = telp;
    }

    public String getDurasi() {
        return durasi;
    }

    public void setDurasi(String durasi) {
        this.durasi = durasi;
    }

    public String getTotal() {
        return total;
    }

    public void setTotal(String total) {
        this.total = total;
    }

    
    
}